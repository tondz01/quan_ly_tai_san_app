import 'dart:convert';

class Country {
  final int id;
  final String name;

  Country({required this.id, required this.name});

  factory Country.fromJson(Map<String, dynamic> json) {
    return Country(id: json['id'], name: json['name']);
  }

  Map<String, dynamic> toJson() {
    return {'id': id, 'name': name};
  }

  static List<Country> fromJsonList(List<dynamic> jsonList) {
    return jsonList.map((json) => Country.fromJson(json)).toList();
  }

  static String encode(List<Country> assets) =>
      json.encode(assets.map((asset) => asset.toJson()).toList());

  static List<Country> decode(String assets) =>
      (json.decode(assets) as List<dynamic>)
          .map<Country>((item) => Country.fromJson(item))
          .toList();
}

